public class Veggies {
    boolean fajitas;
    boolean lettuce;

    //default constructor
    public Veggies() {

    }

    public void SetFajitas(boolean fajitas) {
        this.fajitas = fajitas;
    }

    public boolean isFajitas() {
        return this.fajitas;
    }

    public void SetLettuce(boolean lettuce) {
        this.lettuce = lettuce;
    }

    public boolean isLettuce() {
        return this.lettuce = lettuce;
    }
}
